// 반복 조건이 없으면 무한 반복이 되니까 주의 !

let i = 0;

while (i < 10) {
    document.write('Coding everybody ~! <br />');
    i += 1;
}

// while문을 톺아보자

/*

1. while문은 i 라고 하는 변수, 즉 언제까지 반복을 실행할 것인가에 대한 `초기화용 변수` 가 존재한다.
2. 반복문을 실행할 때마다 초기값을 갱신해주는 `반복 실행` 이라는 부분이 존재한다.
3. 반복을 계속 진행할 것인가 즉, `반복 조건` 이 존재한다.

*/

